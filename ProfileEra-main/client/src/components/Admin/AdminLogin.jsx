import React, { useState } from 'react';
import { motion } from 'framer-motion';
import { Lock, Key, ArrowRight } from 'lucide-react';
import axios from 'axios';

const AdminLogin = ({ onLogin }) => {
    const [apiKey, setApiKey] = useState('');
    const [error, setError] = useState('');
    const [loading, setLoading] = useState(false);

    const handleSubmit = async (e) => {
        e.preventDefault();
        setError('');
        setLoading(true);

        try {
            const apiUrl = import.meta.env.VITE_API_URL || 'http://localhost:5000';
            const response = await axios.get(`${apiUrl}/api/admin/verify`, {
                headers: { 'x-admin-api-key': apiKey }
            });

            if (response.data.message === "Admin access verified") {
                localStorage.setItem('adminKey', apiKey);
                onLogin(apiKey);
            }
        } catch (err) {
            setError(err.response?.data?.error || 'Invalid API Key');
        } finally {
            setLoading(false);
        }
    };

    return (
        <div className="min-h-screen bg-[#F5EEFF] flex items-center justify-center px-4 font-sans">
            <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                className="max-w-md w-full"
            >
                <div className="bg-white rounded-[2.5rem] p-10 shadow-2xl border-2 border-purple-100">
                    <div className="flex flex-col items-center mb-8">
                        <div className="w-20 h-20 bg-purple-100 rounded-3xl flex items-center justify-center mb-6 text-[#6D28D9]">
                            <Lock size={40} />
                        </div>
                        <h2 className="text-3xl font-black text-gray-900 uppercase tracking-wider">Admin Portal</h2>
                        <p className="text-gray-500 font-medium">Enter your credentials to continue</p>
                    </div>

                    <form onSubmit={handleSubmit} className="space-y-6">
                        <div>
                            <label className="block text-sm font-bold text-gray-700 mb-2 ml-1">Admin API Key</label>
                            <div className="relative">
                                <span className="absolute left-4 top-1/2 -translate-y-1/2 text-gray-400">
                                    <Key size={18} />
                                </span>
                                <input
                                    type="password"
                                    placeholder="Enter your secret key"
                                    className="w-full pl-12 pr-4 py-4 border border-gray-200 rounded-xl bg-gray-50 focus:bg-white transition-all focus:ring-2 focus:ring-purple-100 outline-none font-medium"
                                    value={apiKey}
                                    onChange={(e) => setApiKey(e.target.value)}
                                    required
                                />
                            </div>
                        </div>

                        {error && (
                            <motion.p
                                initial={{ opacity: 0 }}
                                animate={{ opacity: 1 }}
                                className="text-red-500 text-sm font-bold text-center bg-red-50 py-2 rounded-lg"
                            >
                                {error}
                            </motion.p>
                        )}

                        <button
                            type="submit"
                            disabled={loading}
                            className="w-full bg-[#6D28D9] text-white py-5 rounded-xl font-bold text-lg hover:bg-[#5B21B6] transition-all shadow-lg shadow-purple-200 flex items-center justify-center gap-2 group disabled:opacity-70 uppercase tracking-widest"
                        >
                            {loading ? 'Verifying...' : 'Login to Dashboard'}
                            <ArrowRight className="group-hover:translate-x-1 transition-transform" />
                        </button>
                    </form>
                </div>

                <p className="mt-8 text-center text-gray-400 text-sm font-medium">
                    ProfileEra Management System v1.0
                </p>
            </motion.div>
        </div>
    );
};

export default AdminLogin;
